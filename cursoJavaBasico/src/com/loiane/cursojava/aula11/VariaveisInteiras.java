package com.loiane.cursojava.aula11;

public class VariaveisInteiras {

	public static void main(String[] args) {
		
		byte idade01 = 12;
		
		short idade02 = 14;
		
		int idade03 = 16;
		
		long idade04 = 18;
		
		System.out.println("Sua idade é idade01 = " + idade01);
		System.out.println("Sua idade é idade02 = " + idade02);
		System.out.println("Sua idade é idade03 = " + idade03);
		System.out.println("Sua idade é idade04 = " + idade04);
		

	}

}
